<p>Step 6: </p>

<p>Group 4. Users who seem to bypass the 14-day free recycle mark by clicking on the 13th or 14th day (the most annoying ones) </p>

<p>There are 2 scenarios here. </p>

<p>Golden and Standards have auto recycle set at 14 days.</p>

<p>Golden packs have auto recycle set from 7 to 12 days, depending on the pack. </p>

<ul class="neobux">
