	<li>Take your time, and find the worst of the worst. Being at the top of the list does not necessarily make them the worst NOW. </li>
</ul>

<p>Step 4: </p>

<p>Group 2. Referrals who click their ads only once in a while</p>

<ul class="neobux">
	<li>There are 2 ways to do this step</li>
	<li>Sort by next payment (lowest first)</li>
	<li>Sort by average (lowest first)</li>
</ul>

<p>Search through the graphs and check who does not click very much. Four ads every 4 days is an average of 1, thus not good. This is group #2. </p>

