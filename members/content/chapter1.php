	<li>Don’t recycle referrals who have not clicked for an extensive period of time (like  days or more), and clicked recently. They will be taken care of in the 6th step. </li>
</ul>

<p>Step 5: </p>

<p>Group 3. Referrals who click many ads, get a great average, last around 30 days, and then die off slowly (normally they have a slowly decreasing average of 1.1 to 1.6) </p>

<p>Sort the referrals by average, and go to those over 1.1. Check each graphs, regardless of their clicks, regardless of their time with you (except if they are a few days old), and recycle those who are bad clickers. People normally associate bad clickers with bad averages, which is not the case. Take your time here. </p>

