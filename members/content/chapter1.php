<h2>1a: Neobux Recycling Strategy</h2>
<hr class="shortTitle" />
<br />

<p>Generally, there are 4 groups of bad clickers. There may be others that are exceptions, but they are
not important.</p>

<p>1. Referrals who always click 2 ads or less<br />
2. Referrals who click their ads only once in a while<br />
3. Referrals who click many ads, have a great average, last around 30 days, and then die off slowly –
normally they have a slowly decreasing average of 1.1 to 1.6<br />
4. Users who seem to bypass the 14-day free recycle mark by clicking on the 12th or 13th day – these
are the most annoying ones<br />
</p>

<p>Let’s pretend that you follow a strategy that tells you to recycle referrals who have an average of
under 1.0</p>

<p>You do so and your average goes up. Here’s what it’ll look like:</p>

<center><img src="images/recycle1.jpg" class="clickable" /></center>

<p>You’ll have taken care of 1 group of referrals (those who click every once in a while). Good for you.
However, your clicks WILL drop even if you continue to use that strategy. Why is that?
</p>

<p>This is why:</p>

<center><img src="images/recycle2.jpg" class="clickable" /></center>

<p>After around one week of using the strategy, that group of referrals will almost be abolished; however, the others increase in numbers. This is why your clicks fall; while you’re paying attention to some of your bad clickers, the others sneak up on you, and make up a large percentage of your total bad clickers. Your average decreases once more, leading you to find another strategy. </p>

<p>How about plain old bad clicks (or group #1)? </p>

<p>You look at your graphs and find many, many of them. Your eyes brighten, and you concentrate on them, recycling as many as you can. Your average skyrockets again, making you happy once more. A week later, they drop again. Here’s why: </p>

<center><img src="images/recycle3.jpg" class="clickable" /></center>

<p>Look at group #2, they rose again. You took your attention off of them, and now, they’ve come back to haunt you. It sucks, right? So you find another strategy, continuing to ride the exact same rollercoaster over and over again, all while wasting time and money. </p>

<p>So why am I telling you this? </p>

<p>Because I have a solution. A solution that takes some time, but pays off. </p>

<p>Each of the 4 groups mentioned above are traceable. They can be monitored. Most of you reading this probably think “I don’t feel like getting this all straightened out”. It’s not a problem, I’ve done that for you.</p>

<p>I’ve divided this process into simple steps. Follow them every day and you’ll surely be on your way to a high average – or at least, better than before. </p>

<p>Step 1: </p>
<p>Do this step whenever you feel like it. </p>

<p>1. Make an amount to spend daily according to your referrals. If you have 500 referrals, spend $0.60 on recycling. </p>

<p>2. If you have 2500 referrals, spend $2.00 on recycling. </p>

<p>3. If you want to spend less, spend less. If you want to spend more, spend more, depending on your budget. </p>

<p>Step 2: </p>

<p>Do this step once a week, optional. </p>

<p>1. Sort the referrals by their referral number. The system will actually be sorting them by their usernames, which are hidden, giving you a random list of referrals. </p>

<p>2. Take 10% of your referrals (example: you have 1000 referrals, take 100), preferably the first ones you see and analyze them. Write down the number of referrals on a piece of paper, which are: </p>

<ul class="neobux">
	<li>Good, no need for recycling </li>
	<li>Graph checked, is clicking low clicks (group #1)</li>
	<li>Graph checked, is not clicking enough days for your standards (group #2) </li>
	<li>Graph checked, was a good referral (judging by his average, normally 1.1 to 1.7), but is dying off (group #3)</li>
	<li>Graph checked, did not click for a long time, but was not auto recycled by the system (group #4) </li>
</ul>

<p>3. Grab a calculator and divide the number of referrals in each group by the total number you counted. </p>

<p>This will give you a number similar to 0.XXX ... this is a percentage. </p>

<p>Example: </p>

<ul class="neobux">
	<li>Of the 10% of my 1000 referrals, 15 were in group 1. 15 / 100 = 0.15, or 15% of my referrals</li>
	<li>Once finished, you have an approximation of the bad referrals, and which groups they are in. Use this information to separate which portion of your spending limit (step 1) you will use for each group. </li>
</ul>


<p>Note:</p>

<ul class="neobux">
	<li>Your referrals change very fast. This is why I recommend you do this step every week at least. Then you’ll have an updated approximation.</li>
	<li>Do not use the referral # to sort more than twice in a row. Members you have not recycled the first week will be in the exact same slot the next week, giving you an inaccurate list. Sort them by 2 or 3 the next week, in order to get a change of referrals.</li>
</ul>

<p>Step 3: </p>

<p>Group 1. Referrals who always click 2 ads or less </p>

<p>Go to your rented referrals list and sort them by their average, lowest first. Check their graphs and recycle them if they click 1 or 2 ads per day. Follow your percentages made on Step 2 to base you’re your spending. This is group #1. 
</p>

