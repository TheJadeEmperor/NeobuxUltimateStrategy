<h2>5a. Elements of a Splash Page</h2>

<p>Whether the splash page you want to create is graphics intensive or not, there are certain elements of a splash page that must exist to make it work. Firstly, there is the eye-grabbing headline – this gets your viewer’s attention. It needs to stand out on the page and compel the viewer to take action.</p>

<p>Below the headline is the optional sub-headline. The sub-headline should be slightly smaller than the main headline, but it should stand out and complement the main headline.</p>

<p>Of course, there is the opt-in form, the whole reason why we are making a splash page in the first place, to capture someone’s name and email. It goes near the bottom of the page. If you’re making a video squeeze page, then the opt-in form goes to the right of the video.</p>

<p>Underneath the opt-in form is the “no spam” message. This is extremely important, as people need to know that you are not going to spam them, but you will send them relevant information. Basically, people are skeptical about joining a mailing list, so they need to know that they are going to get what they signed up for.</p>

<p>Many splash pages have JavaScript built in that shows a pop up window when the viewer closes the page. We do not recommend doing this, because it will annoy the viewer and if they are annoyed, they will never come back. Many people enable pop ups because they think that the viewer might change their minds in the last second.

<p>No matter the reason, pop ups are always annoying and will decrease the quality of user experience. If you want to use pop ups, use them at your own risk.</p>

